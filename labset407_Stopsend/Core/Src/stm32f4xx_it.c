        //   printf("0");
        //   b=b/2;
        // }
        //  printf("\n Num : %ld Time : %ld \n\n ",times,time_end-time_start);
        if(times%6000000==0)
          {
            time_end=HAL_GetTick();
            printf("\n\n\n\n\n\n\n\n\n\n %ld Have spent %ld ms. \n\n\n\n\n\n\n\n\n\n" , times,time_end-time_start);
          }

        break;


        case GPIO_PIN_11://手动触发的第一个中断
        time_start=HAL_GetTick();
        GPIOF->ODR = (GPIOF->ODR & 0xFFE0) | (a & 0x1F);
        HAL_GPIO_WritePin(GPIOF,GPIO_PIN_8,GPIO_PIN_SET);
        HAL_GPIO_WritePin(GPIOF,GPIO_PIN_8,GPIO_PIN_RESET);
